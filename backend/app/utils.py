from typing import List


def generate_recommendations(diagnosis_category: int, cirrhosis_stage: int = None) -> List[str]:
    """Generate medical recommendations based on diagnosis"""

    recommendations = []

    if diagnosis_category == 0:  # Healthy donor
        recommendations = [
            "Результаты анализов в пределах нормы",
            "Рекомендуется плановое профилактическое обследование раз в год",
            "Поддерживайте здоровый образ жизни, сбалансированное питание",
            "Регулярная физическая активность для поддержания здоровья печени"
        ]

    elif diagnosis_category == 1:  # Suspicious donor
        recommendations = [
            "Обнаружены незначительные отклонения от нормы",
            "Рекомендуется повторное обследование через 3 месяца",
            "Исключить алкоголь и жирную пищу",
            "При необходимости - гепатопротекторы (по назначению врача)",
            "Консультация гастроэнтеролога или гепатолога"
        ]

    elif diagnosis_category == 2:  # Hepatitis
        recommendations = [
            "Требуется дополнительное обследование для подтверждения типа гепатита",
            "Анализы на маркеры вирусных гепатитов (HBsAg, anti-HCV)",
            "Противовирусная терапия (по показаниям)",
            "Строгая диета N5 (исключить жареное, острое, жирное)",
            "Полный отказ от алкоголя",
            "Срочная консультация гепатолога"
        ]

    elif diagnosis_category == 3:  # Fibrosis
        recommendations = [
            "Выявлены признаки фиброза печени",
            "Эластометрия печени для определения степени фиброза",
            "Противовоспалительная и антифибротическая терапия",
            "Строгое соблюдение диеты N5",
            "Полный отказ от алкоголя и гепатотоксичных препаратов",
            "Обязательное наблюдение гепатолога (каждые 3-6 месяцев)"
        ]

    elif diagnosis_category == 4:  # Cirrhosis
        recommendations = [
            "ВЫЯВЛЕН ЦИРРОЗ ПЕЧЕНИ - ТРЕБУЕТСЯ НЕМЕДЛЕННАЯ ГОСПИТАЛИЗАЦИЯ",
            "Полное обследование: эндоскопия, эластометрия, МРТ или КТ",
            "Комплексная гепатопротективная и симптоматическая терапия",
            "Строжайшая диета N5 с ограничением белка при асците",
            "Полный отказ от алкоголя на всю жизнь",
            "Диспансерное наблюдение гепатолога (ежемесячно)",
            "Скрининг на гепатоцеллюлярную карциному (каждые 6 месяцев)",
        ]

        if cirrhosis_stage:
            if cirrhosis_stage >= 3:
                recommendations.append("Рассмотреть вопрос о трансплантации печени")
            if cirrhosis_stage >= 4:
                recommendations.append("Декомпенсированный цирроз - лечение в стационаре")

    return recommendations